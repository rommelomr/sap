    public function buscarCliente(){
        $string = $_GET['name'];

        $persona = Cliente::with('persona')->whereHas('persona',function (Builder $query) use ($string){
            $query->where('nombre','like','%'.$string.'%')->orwhere('apellido','like','%'.$string.'%');
        })->get();

        $data = $persona->all();

        echo json_encode($data);
